package com.mco.fr.rest;

import java.util.List;

import javax.ejb.Stateless;
import javax.persistence.EntityManager;
import javax.persistence.NoResultException;
import javax.persistence.PersistenceContext;
import javax.persistence.TypedQuery;
import javax.ws.rs.*;
import javax.ws.rs.core.Response;
import javax.ws.rs.core.Response.Status;
import javax.ws.rs.core.UriBuilder;
import com.mco.fr.entities.Projet;

/**
 * 
 */
@Stateless
@Path("/projets")
public class ProjetEndpoint
{
   @PersistenceContext(unitName = "forge-default")
   private EntityManager em;

   @POST
   @Consumes("application/json")
   public Response create(Projet entity)
   {
      em.persist(entity);
      return Response.created(UriBuilder.fromResource(ProjetEndpoint.class).path(String.valueOf(entity.getId())).build()).build();
   }

   @DELETE
   @Path("/{id:[0-9][0-9]*}")
   public Response deleteById(@PathParam("id") Long id)
   {
      Projet entity = em.find(Projet.class, id);
      if (entity == null)
      {
         return Response.status(Status.NOT_FOUND).build();
      }
      em.remove(entity);
      return Response.noContent().build();
   }

   @GET
   @Path("/{id:[0-9][0-9]*}")
   @Produces("application/json")
   public Response findById(@PathParam("id") Long id)
   {
      TypedQuery<Projet> findByIdQuery = em.createQuery("SELECT DISTINCT p FROM Projet p WHERE p.id = :entityId ORDER BY p.id", Projet.class);
      findByIdQuery.setParameter("entityId", id);
      Projet entity;
      try
      {
         entity = findByIdQuery.getSingleResult();
      }
      catch (NoResultException nre)
      {
         entity = null;
      }
      if (entity == null)
      {
         return Response.status(Status.NOT_FOUND).build();
      }
      return Response.ok(entity).build();
   }

   @GET
   @Produces("application/json")
   public List<Projet> listAll()
   {
      final List<Projet> results = em.createQuery("SELECT DISTINCT p FROM Projet p ORDER BY p.id", Projet.class).getResultList();
      return results;
   }

   @PUT
   @Path("/{id:[0-9][0-9]*}")
   @Consumes("application/json")
   public Response update(Projet entity)
   {
      entity = em.merge(entity);
      return Response.noContent().build();
   }
}